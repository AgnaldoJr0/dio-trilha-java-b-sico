package TiposVariaveis;

public class tiposVariaveis {

    public static void main(String[] args) {
        byte idade = 123;
        short ano = 2024;
        int cep = 12410888; // se começar com 0, talvez tenha que ser outro tipo
        long cpf = 119248191241L; // uma variavel long sempre termina com l e se começar com 0, talvez tenha que ser outro tipo
        float pi = 3.14f; // uma variavel float sempre termina com f
        double salario = 1500.00;
    }
}
